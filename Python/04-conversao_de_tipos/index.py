# exemplos de conversão de tipos em python

preco = 10

print(preco)
# resultado = 10

#convertendo agora:
preco = float(preco)
print(preco)
# resultado 10.0 virou um número flutuante

# para preservar um número inteiro numa divisão em python, podemos utilizar duas barras //

# exemplo:
print(preco //2)
#resultado = 5 -> ou seja um numero inteiro

# para converter para strings usamos o str

# exemplo:
print(str(preco))
